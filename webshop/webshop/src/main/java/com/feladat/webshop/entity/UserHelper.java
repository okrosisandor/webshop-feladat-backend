package com.feladat.webshop.entity;

public class UserHelper {
	
	private String password;

	public UserHelper() {
		super();
	}

	public UserHelper(String password) {
		super();
		this.password = password;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}
	
	

}
